# 1. Даны два целых числа А и В (А < Б). Найти произведение всех целых чисел от А до В включительно.

def task1():
    A = int(input("Введите число A: "))
    B = int(input("Введите число B: "))
    
    if A >= B:
        print("Ошибка: A должно быть меньше B")
        return
    
    product = 1
    for i in range(A, B + 1):
        product *= i
    
    print(f"Произведение чисел от {A} до {B} = {product}")

# 2. Спортсмен-лыжник начал тренировки, пробежав в первый день 10 км. Каждый следующий день он увеличивал 
#    длину пробега на Р процентов от пробега предыдущего дня (Р — вещественное, 0< Р <50). По данному Р определить, 
#    после какого дня суммарный пробег лыжника за все дни превысит 200 км, и вывести найденное количество дней К 
#    (целое) и суммарный пробег S (вещественное число).

def task2():
    P = float(input("Введите процент P (0 < P < 50): "))
    
    if P <= 0 or P >= 50:
        print("Ошибка: P должно быть в диапазоне (0, 50)")
        return
    
    daily_run = 10.0
    total_run = 10.0
    days = 1
    
    while total_run <= 200:
        days += 1
        daily_run *= (1 + P / 100)
        total_run += daily_run
    
    print(f"Количество дней K = {days}")
    print(f"Суммарный пробег S = {total_run:.2f} км")

def main():
    print("=== ЗАДАЧА 1 ===")
    task1()
    
    print("\n=== ЗАДАЧА 2 ===")
    task2()

if __name__ == "__main__":
    main()