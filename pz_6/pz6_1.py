# Дан целочисленный список размера 10. Вывести все содержащиеся в данном списке нечетные числа в порядке возрастания их индексов, а так же их количество K.

import random

# Создаем список из 10 случайных целых чисел
numbers = [random.randint(1, 100) for i in range(10)]
# random.randint(1, 100) генерирует случайное целое число от 1 до 100 (включительно).
# for _ in range(10) создает цикл, который повторяется 10 раз. Здесь _ — это переменная, которая не используется, так как мы просто хотим выполнить 10 итераций.
# В итоге, [random.randint(1, 100) for _ in range(10)] создает список из 10 случайных чисел

# Выводим исходный список
print("Исходный список:", numbers)

# Фильтруем нечетные числа
odd_numbers = [num for num in numbers if num % 2 != 0]
# num for num in numbers — это цикл, который перебирает каждый элемент num в списке numbers.
# if num % 2 != 0 — это условие, которое проверяет, является ли число num нечетным. 
# Оператор % возвращает остаток от деления, и если остаток от деления на 2 не равен 0, то число нечетное.
# В итоге, [num for num in numbers if num % 2 != 0] создает список только из нечетных чисел

# Выводим нечетные числа
print("Нечетные числа:", odd_numbers)

# Выводим количество нечетных чисел
print("Количество нечетных чисел (K):", len(odd_numbers))