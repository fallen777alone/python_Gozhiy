# Дан список размера N. Возвести в квадрат все его локальные минимумы (то есть числа, меньшие своих соседей).

import random

def square_local_minima(lst):
    """
    Функция для возведения в квадрат всех локальных минимумов в списке.
    Локальный минимум — это элемент, который меньше своих соседей.
    """
    # Создаем копию списка, чтобы избежать изменения исходного списка
    result = lst.copy()

    # Проверяем каждый элемент списка, кроме первого и последнего
    for i in range(1, len(result) - 1):
        if result[i] < result[i - 1] and result[i] < result[i + 1]:
            result[i] **= 2  # Возводим локальный минимум в квадрат

    return result


# Задаем размер списка
try:
    while True:
        N = int(input("Введите размер списка: "))
        if N < 0:
            print("Размер списка не может быть отрицательным!")
            continue
        break


    # Генерируем случайный список размера N
    numbers = [random.randint(1, 100) for _ in range(N)]

    # Выводим сгенерированный список
    print("Исходный список:", numbers)

    # Вызываем функцию
    modified_list = square_local_minima(numbers)

    # Выводим измененный список
    print("Список после возведения локальных минимумов в квадрат:", modified_list)
except ValueError:
    print("Ошибка при вводе данных!")