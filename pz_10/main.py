# Туристические агентства предлагают следующие туры. Вояж – Мексика,Канада,Израиль,
# Италия,США. РейнаТур – Англия,Япония,Канада,ЮАР. Радуга – США,Испания,Швеция,
# Австралия.
# Определить в каких турагенствах можно приобрести туры в Канаду, а в каких в США.

# Словарь с турагентствами и их турами
agencies = {
    'Вояж': {'Мексика', 'Канада', 'Израиль', 'Италия', 'США'},
    'РейнаТур': {'Англия', 'Япония', 'Канада', 'ЮАР'},
    'Радуга': {'США', 'Испания', 'Швеция', 'Австралия'}
}

# Функция для поиска агентств, предлагающих определенную страну
def find_agencies(country):
    result = []
    for agency, countries in agencies.items():
        if country in countries:
            result.append(agency)
    return result

# Поиск агентств с Канадой и США
canada_agencies = find_agencies('Канада')
usa_agencies = find_agencies('США')

# Вывод результатов
print("Туры в Канаду можно приобрести в следующих агентствах:", ', '.join(canada_agencies))
print("Туры в США можно приобрести в следующих агентствах:", ', '.join(usa_agencies))