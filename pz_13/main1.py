# В двумерном списке элементы строки N (N задать с клавиатуры) увеличить на 3.

# Создаем двумерный список (матрицу) для примера
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Выводим исходную матрицу
print("Исходная матрица:")
for row in matrix:
    print(row)

# Запрашиваем номер строки у пользователя
N = int(input("\nВведите номер строки для увеличения (от 0 до {}): ".format(len(matrix)-1)))

# Проверяем корректность введенного номера строки
if 0 <= N < len(matrix):
    # Увеличиваем каждый элемент строки N на 3
    for i in range(len(matrix[N])):
        matrix[N][i] += 3
    
    # Выводим измененную матрицу
    print("\nМатрица после изменения строки", N)
    for row in matrix:
        print(row)
else:
    print("Ошибка! Номер строки должен быть от 0 до", len(matrix)-1)