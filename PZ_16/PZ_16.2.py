# Создание базового класса "Транспортное средство" и его наследование для создания
# классов "Автомобиль" и "Мотоцикл". В классе "Транспортное средство" будут
# общие свойства, такие как максимальная скорость и количество колес, а классы-
# наследники будут иметь свои уникальные свойства и методы.

class ТранспортноеСредство:
    def __init__(self, название, максимальная_скорость, количество_колес):
        self.название = название
        self.максимальная_скорость = максимальная_скорость
        self.количество_колес = количество_колес

    def описание(self):
        return (f"{self.название}: "
                f"Макс. скорость - {self.максимальная_скорость} км/ч, "
                f"Кол-во колес - {self.количество_колес}")


class Автомобиль(ТранспортноеСредство):
    def __init__(self, название, максимальная_скорость, количество_дверей, тип_кузова):
        super().__init__(название, максимальная_скорость, 4)
        self.количество_дверей = количество_дверей
        self.тип_кузова = тип_кузова

    def открыть_багажник(self):
        return f"Багажник {self.название} открыт"

    def описание(self):
        return (super().описание() +
                f", Дверей - {self.количество_дверей}, "
                f"Тип кузова - {self.тип_кузова}")


class Мотоцикл(ТранспортноеСредство):
    def __init__(self, название, максимальная_скорость, есть_коляска):
        super().__init__(название, максимальная_скорость, 2)
        self.есть_коляска = есть_коляска

    def сделать_поворот(self):
        return f"{self.название} делает поворот!"

    def описание(self):
        коляска = "с коляской" if self.есть_коляска else "без коляски"
        return super().описание() + f", {коляска}"


if __name__ == "__main__":
    транспорт1 = ТранспортноеСредство("Грузовик", 120, 6)
    автомобиль1 = Автомобиль("Toyota Camry", 220, 4, "седан")
    мотоцикл1 = Мотоцикл("Harley Davidson", 180, True)

    print("=== Описание транспортных средств ===")
    print(транспорт1.описание())
    print(автомобиль1.описание())
    print(мотоцикл1.описание())

    print("\n=== Уникальные методы ===")
    print(автомобиль1.открыть_багажник())
    print(мотоцикл1.сделать_поворот())